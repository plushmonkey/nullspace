u32 crc32_map(const u8* ptr, size_t size) {
  u32 crc = 0xFFFFFFFF;

  for (size_t i = 0; i < size; ++i) {
    u8 tile = ptr[i];

    if (tile != 0 && (tile < 0xA1 || tile == 0xAB)) {
      crc = (crc >> 8) ^ g_crc32_reflected_table[crc & 0xFF ^ (u32)tile];
    }
  }

  return crc;
}

u32 SettingsChecksum(u32 key, const ArenaSettings& settings) {
  u32* data = (u32*)&settings;
  u32 sum = 0;

  for (int i = 0; i < sizeof(settings) / sizeof(u32); ++i, ++data) {
    sum += (*data ^ key);
  }

  return sum;
}

u8 WeaponChecksum(const u8* data, size_t size) {
  u8 checksum = 0;

  for (size_t i = 0; i < size; ++i) {
    checksum ^= data[i];
  }

  return checksum;
}

